    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        
        String nombre;
        int contra;
       
        nombre = TFnombre.getText();
        contra = new String(TFpasswd.getPassword()).hashCode();
        int id = c.IniciarSesion(nombre, contra);
                
        if(id>0){
            setVisible(false);
            dispose();
            
            JOptionPane.showMessageDialog(null,"Sesi칩n iniciada: "+nombre);
            
            MainView mainView = new MainView(nombre, id); 
            
            if (mv != null){
            
                mv.dispose();
            } else {
                mainView.dispose();
            }
            
            mainView.setVisible(true);
        } 
    }//GEN-LAST:event_btnLogInActionPerformed

